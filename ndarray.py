import numpy as np

# Create a 1D array
array_1d = np.array([1, 2, 3, 4, 5])
print(array_1d)

# Create a 2D array
array_2d = np.array([[1, 2, 3], [4, 5, 6]])
print(array_2d)

# Create a 3x3 array of zeros
array_zeros = np.zeros((3, 3))
print(array_zeros)

# Create a 2x4 array of ones
array_ones = np.ones((2, 4))
print(array_ones)

# Create an array with values from 0 to 9
array_range = np.arange(10)
print(array_range)

# Create a 3x3 array with random values
array_random = np.random.random((3, 3))
print(array_random)